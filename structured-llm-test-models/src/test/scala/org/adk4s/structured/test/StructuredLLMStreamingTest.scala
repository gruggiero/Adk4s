  test("streamWithResultRaw does not inject schema") {
    val jsonResponse: String = """{"name": "Alice Green", "skills": ["Python"], "seniority": "MID"}"""

    val mockClient: LLMClient = new LLMClient:
      override def complete(
        conversation: Conversation,
