        // Emit as single chunk
        onChunk(StreamedChunk(
          id = "mock-chunk-id",
          content = Some(malformedJson),
          toolCall = None,
          finishReason = Some("stop"),
          thinkingDelta = None
        ))
        complete(conversation, options)

      override def getContextWindow(): Int = 8192
      override def getReserveCompletion(): Int = 512

